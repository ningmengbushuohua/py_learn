# 三.JSON语法

# 1. JSON语法规则
'''
    a. 数据在键值对中
    b.数据由逗号分隔
    c.json 数据使用花括号{} 保存数据
    d. 中括号[] 保存数组
'''

# 2. JSON的数据类型
'''
    a.字符串，JSON中的字符串必须用双引号包围
    b.数字： JSON的数字必须是整型 或浮点型
    c.对象（JSON对象）： JSON中的值可以是对象（使用{}的均为对象）
    d.数组：JSON中的值可以是数组，数组就是使用[] 括起开
    e. 布尔：JSON中的值可以是布尔值：true 或false
    f. null: JSON 中的值可以是null
    
    注意： JSON中的键 必须是字符串类型，值可以是任意类型
'''